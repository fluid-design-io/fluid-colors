import numpy as np
import os


def fit_polynomial(color_data, order):
    x = list(range(len(color_data)))
    y = list(color_data.values())

    coefficients = np.polyfit(x, y, order)
    polynomial = np.poly1d(coefficients)

    return polynomial


def generate_js_function(polynomial):
    js_function = "(x) => "
    terms = []

    for i, coef in enumerate(polynomial.coefficients):
        if coef != 0:
            exponent = len(polynomial.coefficients) - i - 1
            term = f"{coef:.4f} * Math.pow(x, {exponent})"
            terms.append(term)

    js_function += " + ".join(terms)
    return js_function


